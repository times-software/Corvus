# Collect a list of all the unique edge symbols
def symb_list():
  __Symb_List = []
  for El in __Feff_Edges_Database.keys():
    __Edges = __Feff_Edges_Database[El]
    __Symb_List.extend([ Edge[0] for Edge in __Edges ])
  __Symb_List = list(set(__Symb_List))
  __Symb_List.sort()
  return __Symb_List

